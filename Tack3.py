import re

def normalize_phone(phone_number: str) -> str:
    """
    Нормалізує телефонний номер:
    - залишає тільки цифри та '+'
    - якщо номер без міжнародного коду, додає '+38' (Україна)
    - якщо номер починається з '380', додає лише '+'
    """

    # 1. Залишаємо тільки цифри та '+' (видаляємо всі інші символи)
    cleaned = re.sub(r"[^\d+]", "", phone_number)

    # 2. Якщо номер починається з '+'
    if cleaned.startswith("+"):
        # Якщо вже має правильний код '+38...' — просто повертаємо
        if cleaned.startswith("+38"):
            return cleaned
        else:
            # Наприклад, '+050...' → прибираємо '+'
            cleaned = cleaned[1:]

    # 3. Якщо номер починається з '380' (тобто код є, але без '+')
    if cleaned.startswith("380"):
        return "+" + cleaned

    # 4. В усіх інших випадках вважаємо, що коду немає → додаємо '+38'
    return "+38" + cleaned


if __name__ == "__main__":
    raw_numbers = [
        "067\\t123 4567",
        "(095) 234-5678\\n",
        "+380 44 123 4567",
        "380501234567",
        "+38(050)123-32-34",
        " 0503451234",
        "(050)8889900",
        "38050-111-22-22",
        "38050 111 22 11",
    ]

    sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
    print("Нормалізовані номери телефонів для SMS-розсилки:")
    print(sanitized_numbers)

